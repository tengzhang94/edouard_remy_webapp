    function set_active($menutitle) {
        foreach ($this->menu_items as &$item) { // reference to item!!
            if (strcasecmp($menutitle, $item['name']) == 0) {
                $item['className'] = 'item active';
            } else {
                $item['className'] = 'item inactive';
            }
        }
    }
     
    function get_menuitems($menutitle) {
